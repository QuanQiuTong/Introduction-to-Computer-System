                B[x + 5][j + 1] = B[x + 1][j + 5];
                B[x + 5][j + 2] = B[x + 2][j + 5];
                B[x + 5][j + 3] = B[x + 3][j + 5];
                B[x + 5][j + 4] = B[x + 4][j + 5];
                B[x][j + 5] = x1;
                B[x + 1][j + 5] = x2;
                B[x + 2][j + 5] = x3;
                B[x + 3][j + 5] = x4;
                B[x + 4][j + 5] = x5;
