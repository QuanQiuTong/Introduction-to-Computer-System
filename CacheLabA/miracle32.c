                x2 = A[x + 4][j + 1];
                x3 = A[x + 4][j + 2];
                x4 = A[x + 4][j + 3];
                x5 = A[x + 4][j + 4];
                x6 = A[x + 4][j + 5];
                x7 = A[x + 4][j + 6];
                x8 = A[x + 4][j + 7];

                B[x + 4][j] = B[x][j + 4];
                B[x + 4][j + 1] = B[x + 1][j + 4];
                B[x + 4][j + 2] = B[x + 2][j + 4];
                B[x + 4][j + 3] = B[x + 3][j + 4];
                B[x][j + 4] = x1;
                B[x + 1][j + 4] = x2;
                B[x + 2][j + 4] = x3;
                B[x + 3][j + 4] = x4;
                B[x + 4][j + 4] = x5;
                B[x + 4][j + 5] = x6;
