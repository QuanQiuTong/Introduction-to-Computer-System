                x7 = A[x + 6][j + 6];
                x8 = A[x + 6][j + 7];

                B[x + 6][j] = B[x][j + 6];
                B[x + 6][j + 1] = B[x + 1][j + 6];
                B[x + 6][j + 2] = B[x + 2][j + 6];
                B[x + 6][j + 3] = B[x + 3][j + 6];
                B[x + 6][j + 4] = B[x + 4][j + 6];
                B[x + 6][j + 5] = B[x + 5][j + 6];
                B[x][j + 6] = x1;
                B[x + 1][j + 6] = x2;
                B[x + 2][j + 6] = x3;
