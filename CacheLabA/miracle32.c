                x3 = A[x + 2][j + 2];
                x4 = A[x + 2][j + 3];
                x5 = A[x + 2][j + 4];
                x6 = A[x + 2][j + 5];
                x7 = A[x + 2][j + 6];
                x8 = A[x + 2][j + 7];

                B[x + 2][j] = B[x][j + 2];
                B[x + 2][j + 1] = B[x + 1][j + 2];
                B[x][j + 2] = x1;
                B[x + 1][j + 2] = x2;
                B[x + 2][j + 2] = x3;
                B[x + 2][j + 3] = x4;
                B[x + 2][j + 4] = x5;
                B[x + 2][j + 5] = x6;
                B[x + 2][j + 6] = x7;
                B[x + 2][j + 7] = x8;

                x1 = A[x + 3][j];
                x2 = A[x + 3][j + 1];
                x3 = A[x + 3][j + 2];
                x4 = A[x + 3][j + 3];
                x5 = A[x + 3][j + 4];
                x6 = A[x + 3][j + 5];
                x7 = A[x + 3][j + 6];
                x8 = A[x + 3][j + 7];

                B[x + 3][j] = B[x][j + 3];
                B[x + 3][j + 1] = B[x + 1][j + 3];
                B[x + 3][j + 2] = B[x + 2][j + 3];
                B[x][j + 3] = x1;
                B[x + 1][j + 3] = x2;
                B[x + 2][j + 3] = x3;
                B[x + 3][j + 3] = x4;
                B[x + 3][j + 4] = x5;
                B[x + 3][j + 5] = x6;
                B[x + 3][j + 6] = x7;
                B[x + 3][j + 7] = x8;

